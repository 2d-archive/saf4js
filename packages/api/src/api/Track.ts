export interface Track {

  /**
   * The title of this track.
   */
  title: string;

  /**
   * The author of this track.
   */
  author: string;

  /**
   * The URI of this track.
   */
  uri: string;

  /**
   * The identifier for this track.
   */
  identifier: string;

  /**
   * The length of this track.
   */
  length: bigint;

}
